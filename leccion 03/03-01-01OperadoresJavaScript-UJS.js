let a = 3, b = 2;
let z = a + b;

//Incremento
//Pre incremento 

z = ++a;
console.log(a);
console.log(z);

// post incremento 

z = b++;
console.log(b);
console.log(z);


// Decremento
//predecremento
z = --a;
console.log(a);
console.log(z);


//postdecremento
z = b--;
console.log(b);
console.log(z);


